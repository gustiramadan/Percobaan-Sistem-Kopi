    private DefaultTableModel tabmode;
    private void tampilgaji(){
        Object[] baris = {"No","Nik","Nama karyawan","Jabatan","Gaji pokok","Tunjangan","Uang makan","Bonus","Potongan","Gaji bersih"};
        
        tabmode = new DefaultTableModel(null,baris);
        tabmode.setRowCount(0);
         
         try {
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM t_penggajian";
            ResultSet rs = stmt.executeQuery(query);
            int no = 1;
            while (rs.next()){
                String Nik = rs.getString("t_penggajian.nik");
                String Nama_karyawan = rs.getString("t_penggajian.nama_karyawan");
                String Jabatan = rs.getString("t_penggajian.jabatan");
                String Gaji_Pokok = rs.getString("t_penggajian.gaji_pokok");
                String Tunjangan = rs.getString("t_penggajian.tunjangan");
                String Uang_makan = rs.getString("t_penggajian.uang_makan");
                String Bonus = rs.getString("t_penggajian.bonus");
                String Potongan = rs.getString("t_penggajian.potongan");
                String Gaji_bersih = rs.getString("t_penggajian.gaji_bersih");
                String[] data = {no+"",Nik,Nama_karyawan,Jabatan,Gaji_Pokok,Tunjangan,Uang_makan,Bonus,Potongan,Gaji_bersih};
                no++;
                tabmode.addRow(data);
            }
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
         table_gaji.setModel(tabmode);
    }
