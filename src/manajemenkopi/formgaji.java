    public void EditData()
    {
        String nik = Nik.getText();
        String nama = nama_karyawan.getText();
        String jabatan = Cbjabatan.getSelectedItem().toString();
        String Gaji_pokok = gaji_pokok.getText();
        String Tunjangan = tunjangan.getText();
        String Uang_makan = uang_makan.getText();
        String Bonus = bonus.getText();
        String Potongan = potongan.getText();
        String Gaji_bersih = gaji_bersih.getText();
        
        try
        {
            Statement stmt = koneksi.createStatement();
            String query = "UPDATE t_penggajian SET "+ "uang_makan='"+Uang_makan+"',"+"nama_karyawan='"+nama+"',"+"jabatan='"+jabatan+"',"+"gaji_pokok='"+Gaji_pokok+"',"+"tunjangan='"+Tunjangan+"',"+"bonus='"+Bonus+"',"+"potongan='"+Potongan+"',"+"gaji_bersih='"+Gaji_bersih+"' "+ "WHERE NIK = '"+nik+"'";
            System.out.println(query);
            int berhasil = stmt.executeUpdate(query);
            if(berhasil == 1)
            {
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
            }
        }
        catch (SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada Database");
        }
    }
    private void Btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_simpanActionPerformed

        String nik = Nik.getText();
        String nama = nama_karyawan.getText();
        String jabatan = Cbjabatan.getSelectedItem().toString();
        String Gaji_pokok = gaji_pokok.getText();
        String Tunjangan = tunjangan.getText();
        String Uang_makan = uang_makan.getText();
        String Bonus = bonus.getText();
        String Potongan = potongan.getText();
        String Gaji_bersih = gaji_bersih.getText();

        try{
            Statement stmt = koneksi.createStatement();
            String query = "INSERT INTO t_penggajian"+ "(nik,nama_karyawan,jabatan,gaji_pokok,tunjangan,uang_makan,bonus,potongan,gaji_bersih)"+"VALUES('"+nik+"', '"+nama+"', '"+jabatan+"', '"+Gaji_pokok+"', '"+Tunjangan+"', '"+Uang_makan+"', '"+Bonus+"', '"+Potongan+"', '"+Gaji_bersih+"')";
            System.out.print(query);
            int berhasil = stmt.executeUpdate(query);
            if (berhasil == 1) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dimasukkan");
                tampilgaji();
                reset();
            }else{
                JOptionPane.showMessageDialog(null, "Data Gagal Dimasukkan");
                reset();
            }
            koneksi.close();
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Pada Database");
            reset();
        }
    }//GEN-LAST:event_Btn_simpanActionPerformed

