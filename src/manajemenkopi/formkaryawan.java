    private void Btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_simpanActionPerformed

       String id_karyawan = Id_karyawan.getText();
       String nik = Nik.getText();
       String nama = Nama.getText();
       String jk = Cbjk.getSelectedItem().toString();
       String tempat_lahir = Tempat_lahir.getText();
       java.util.Date tanggal_lahir1 = Tanggal_lahir.getDate();
       java.sql.Date tanggal_lahir = new java.sql.Date(tanggal_lahir1.getTime());
       String pendidikan = Cbpendidikan.getSelectedItem().toString();
       java.util.Date tanggal_masuk1 = Tanggal_masuk.getDate();
       java.sql.Date tanggal_masuk = new java.sql.Date(tanggal_masuk1.getTime());
       String jabatan = Cbjabatan.getSelectedItem().toString();
       String status = Cbstatus.getSelectedItem().toString();
       String agama = Cbagama.getSelectedItem().toString();
       String kontak = Kontak.getText();
       String usia = Usia.getText();
       String alamat = Alamat.getText();
       
       try{
           Statement stmt = koneksi.createStatement();
           String query = "INSERT INTO t_karyawan(id_karyawan,nik,nama,jk,tempat_lahir,tanggal_lahir,pendidikan,tanggal_masuk,jabatan,status,agama,kontak,usia,alamat)"+"VALUES('"+id_karyawan+"', '"+nik+"', '"+nama+"', '"+jk+"', '"+tempat_lahir+"', '"+tanggal_lahir+"', '"+pendidikan+"', '"+tanggal_masuk+"', '"+jabatan+"', '"+status+"', '"+agama+"', '"+kontak+"', '"+usia+"', '"+alamat+"')";
           System.out.print(query);
           int berhasil = stmt.executeUpdate(query);
           if (berhasil == 1) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dimasukkan");
                tampilpegawai();
                reset();
            }else{
                JOptionPane.showMessageDialog(null, "Data Gagal Dimasukkan");
                reset();
            }
             koneksi.close();
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Pada Database");
            reset();
        }
    }//GEN-LAST:event_Btn_simpanActionPerformed

    private void b_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_DeleteActionPerformed
        // TODO add your handling code here:
        String idWhoWantToBeDelete = table_pegawai.getValueAt(baris, 1).toString();
        try {
            Statement stmt = koneksi.createStatement();
            String query = "DELETE FROM t_karyawan WHERE id_karyawan = '"+idWhoWantToBeDelete+"';";
            int berhasil = stmt.executeUpdate(query);
            if (berhasil == 1) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                tabmode.getDataVector().removeAllElements();
                tampilpegawai();
                reset();
            }  
            koneksi.close();
        } catch (SQLException ex){
            ex.printStackTrace();
        }
       
    }//GEN-LAST:event_b_DeleteActionPerformed

        
