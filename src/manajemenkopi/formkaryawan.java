    private void tampilpegawai(){
        Object[] baris = {"No","Id karyawan","NIK","Nama","Jenis kelamin","Tempat lahir","Tanggal lahir","Pendidikan","Tanggal masuk","Jabatan","Status","Agama","Kontak","Usia","Alamat"};
        
        tabmode = new DefaultTableModel(null,baris);
        tabmode.setRowCount(0);
         
         try {
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM t_karyawan";
            ResultSet rs = stmt.executeQuery(query);
            int no = 1;
            while (rs.next()){
                String id_karyawan = rs.getString("id_karyawan");
                String nik = rs.getString("nik");
                String nama = rs.getString("nama");
                String jk = rs.getString("jk");
                String tempat_lahir = rs.getString("tempat_lahir");
                String tanggal_lahir = rs.getString("tanggal_lahir");
                String pendidikan = rs.getString("pendidikan");
                String tanggal_masuk = rs.getString("tanggal_masuk");
                String jabatan = rs.getString("jabatan");
                String status = rs.getString("status");
                String agama = rs.getString("agama");
                String kontak = rs.getString("kontak");
                String usia = rs.getString("usia");
                String alamat = rs.getString("alamat");
                String[] data = {no+"",id_karyawan,nik,nama,jk,tempat_lahir,tanggal_lahir,pendidikan,tanggal_masuk,jabatan,status,agama,kontak,usia,alamat};
                no++;
                tabmode.addRow(data);
            }
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
         table_pegawai.setModel(tabmode);
    }
    
