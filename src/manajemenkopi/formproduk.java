    private DefaultTableModel tabmode;
    private void tampilproduk(){
        Object[] baris = {"No","Id produk","Nama produk","Harga produk","Jumlah produk","Stok produk","Status produksi"};
        
        tabmode = new DefaultTableModel(null,baris);
        tabmode.setRowCount(0);
         
         try {
            Statement stmt = koneksi.createStatement();
            String query = "SELECT * FROM t_produk";
            ResultSet rs = stmt.executeQuery(query);
            int no = 1;
            while (rs.next()){
                String id_produk = rs.getString("t_produk.id_produk");
                String nama_produk = rs.getString("t_produk.nama_produk");
                String harga_produk = rs.getString("t_produk.harga_produk");
                String jumlah_produk = rs.getString("t_produk.jumlah_produk");
                String stok_produk = rs.getString("t_produk.stok_produk");
                String status_produksi = rs.getString("t_produk.status_produksi");
                String[] data = {no+"",id_produk,nama_produk,harga_produk,jumlah_produk,stok_produk,status_produksi};
                no++;
                tabmode.addRow(data);
            }
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Menampilkan data GAGAL", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
         table_produk.setModel(tabmode);
    }

