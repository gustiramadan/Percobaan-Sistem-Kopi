    public void EditData()
    {
        String id_produk = Id_produk.getText();
        String nama_produk = Nama_produk.getText();
        String harga_produk = Harga_produk.getText();
        String jumlah_produk = Stok_produk1.getText();
        String stok_produk = Stok_produk.getText();
        String status_produksi = Cbstatus_produksi.getSelectedItem().toString();
        
        try
        {
            Statement stmt = koneksi.createStatement();
            String query = "UPDATE t_produk SET nama_produk='"+nama_produk+"',"+"harga_produk='"+harga_produk+"',"+"jumlah_produk='"+jumlah_produk+"',"+"stok_produk='"+stok_produk+"',"+"status_produksi='"+status_produksi+"' WHERE id_produk = '"+id_produk+"'";
            System.out.println(query);
            int berhasil = stmt.executeUpdate(query);
            if(berhasil == 1)
            {
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
            }
        }
        catch (SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan pada Database");
        }
    }
    private void Btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_simpanActionPerformed

        String id_produk = Id_produk.getText();
        String nama_produk = Nama_produk.getText();
        String harga_produk = Harga_produk.getText();
        String jumlah_produk = Stok_produk1.getText();
        String stok_produk = Stok_produk.getText();
        String status_produksi = Cbstatus_produksi.getSelectedItem().toString();

        try{
            Statement stmt = koneksi.createStatement();
            String query = "INSERT INTO t_produk"+ "(id_produk,nama_produk,harga_produk,jumlah_produk,stok_produk,status_produksi)"+"VALUES('"+id_produk+"', '"+nama_produk+"', '"+harga_produk+"', '"+jumlah_produk+"', '"+stok_produk+"', '"+status_produksi+"')";
            System.out.print(query);
            int berhasil = stmt.executeUpdate(query);
            if (berhasil == 1) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dimasukkan");
                tampilproduk();
                reset();
            }else{
                JOptionPane.showMessageDialog(null, "Data Gagal Dimasukkan");
                reset();
            }
            koneksi.close();
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Pada Database");
            reset();
        }
    }//GEN-LAST:event_Btn_simpanActionPerformed

